package com.example.cardstackview;
public class Certificado {
    public String nome, instituicao, ano, duracao, descricao;
    public Certificado(String nome, String instituicao, String ano, String duracao, String descricao) {
        this.nome = nome;
        this.instituicao = instituicao;
        this.ano = ano;
        this.duracao = duracao;
        this.descricao = descricao;
    }

}

