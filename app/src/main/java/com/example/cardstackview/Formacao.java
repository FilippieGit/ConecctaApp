package com.example.cardstackview;

public class Formacao {
    public String curso, instituicao, anoInicio, anoConclusao, descricao;
    public Formacao(String curso, String instituicao, String anoInicio, String anoConclusao, String descricao) {
        this.curso = curso;
        this.instituicao = instituicao;
        this.anoInicio = anoInicio;
        this.anoConclusao = anoConclusao;
        this.descricao = descricao;
    }
}
